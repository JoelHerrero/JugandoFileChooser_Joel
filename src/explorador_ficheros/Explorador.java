    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed

        JFileChooser borrar = new JFileChooser("System.getProperty(“user.dir”);");
        borrar.setDialogTitle("Selección de archivos");
        borrar.setFileFilter(filtro1);
        borrar.setFileFilter(filtro2);
        borrar.setFileFilter(filtro3);
        borrar.setFileFilter(filtro4);
        seleccionado = borrar.showDialog(null, "Borrar");

        if (seleccionado == JFileChooser.APPROVE_OPTION) {

            int siono = JOptionPane.showConfirmDialog(null, "¿Desea borrar el archivo?", "", JOptionPane.YES_NO_OPTION);

            if (siono == JOptionPane.YES_OPTION) {
                File fichero = borrar.getSelectedFile();
                fichero.delete();

            } else {
                System.out.println("Adios");
            }

        } else {
            System.out.println("Adios");
        }

    }//GEN-LAST:event_btnBorrarActionPerformed

