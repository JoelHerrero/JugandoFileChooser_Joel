    private void btnCopiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCopiarActionPerformed
        JFileChooser copiar = new JFileChooser("System.getProperty(“user.dir”);");

        copiar.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);

        JFileChooser copiar2 = new JFileChooser("C:\\Users\\Joel\\Documents\\carpetaPruebas");
        copiar2.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        copiar2.setDialogTitle("Elegir destino");

        copiar.setDialogTitle("Selección de archivos");
        copiar.setFileFilter(filtro1);
        copiar.setFileFilter(filtro2);
        copiar.setFileFilter(filtro3);
        copiar.setFileFilter(filtro4);
        seleccionado = copiar.showDialog(null, "copiar a...");

        if (seleccionado == JFileChooser.APPROVE_OPTION) {
            File archivoacopiar = copiar.getSelectedFile();
            String rutaOrig = archivoacopiar.getPath();
            Path origen = Paths.get(rutaOrig);

            seleccionado2 = copiar2.showDialog(null, "copiar");

            if (seleccionado2 == JFileChooser.APPROVE_OPTION) {
                
                File archivoCopiado = copiar2.getSelectedFile();
                
                String rutaDestino = archivoCopiado.getPath();
                
                Path destino = Paths.get(rutaDestino);
                try {
                    Files.copy(origen, destino, REPLACE_EXISTING);
                    System.out.println("Se copió correctamente");
                } catch (IOException ex) {
                    Logger.getLogger(Explorador.class.getName()).log(Level.SEVERE, null, ex);
                }

            }else {
                System.out.println("No se pudo copiar el archivo");
            }
        }


    }//GEN-LAST:event_btnCopiarActionPerformed

